# ── Expansion : (hanzi, ton, pinyin_display) ────────────────

SYLLABLES: list[tuple[str, int, str]] = []
for base, tones in HANZI_MAP.items():
    for tone, hanzi in sorted(tones.items()):
        SYLLABLES.append((hanzi, tone, f"{base}{tone}"))


# ── Génération ──────────────────────────────────────────────

async def generate_one(
    voice: str, hanzi: str, pinyin: str, out_dir: Path, sem: asyncio.Semaphore
) -> tuple[str, bool, str]:
    """Génère un WAV via Edge TTS. Retourne (path, ok, err_msg)."""
    out_path = out_dir / f"{pinyin}.wav"
    if out_path.exists():
        return str(out_path), True, "skip"

    for attempt in range(1, MAX_RETRIES + 1):
        try:
            async with sem:
                comm = edge_tts.Communicate(hanzi, voice)
                await comm.save(str(out_path))
            return str(out_path), True, ""
        except Exception as exc:
            if attempt == MAX_RETRIES:
                return str(out_path), False, f"{exc}"
            await asyncio.sleep(1 * attempt)

    return str(out_path), False, "max retries"


async def generate_voice(voice: str, sem: asyncio.Semaphore) -> tuple[int, int, int]:
    """Génère tous les WAV pour une voix. Retourne (ok, skip, fail)."""
    short_name = voice.replace("zh-CN-", "").replace("Neural", "")
    out_dir = DATASET_DIR / short_name
    out_dir.mkdir(parents=True, exist_ok=True)

    tasks = []
    for hanzi, _tone, pinyin in SYLLABLES:
        tasks.append(generate_one(voice, hanzi, pinyin, out_dir, sem))

    results = await asyncio.gather(*tasks)

    ok = sum(1 for _, s, m in results if s and m != "skip")
    skip = sum(1 for _, s, m in results if s and m == "skip")
    fail = sum(1 for _, s, _ in results if not s)

    for path, success, msg in results:
        if not success:
            print(f"  FAIL  {path}  — {msg}", file=sys.stderr)

    return ok, skip, fail


async def main():
    print(f"Syllabes : {len(SYLLABLES)}")
    print(f"Voix     : {len(VOICES)}")
    print(f"Total    : {len(SYLLABLES) * len(VOICES)} fichiers")
    print()

    # Écrire syllables.csv
    DATASET_DIR.mkdir(parents=True, exist_ok=True)
    csv_path = DATASET_DIR / "syllables.csv"
    with open(csv_path, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["hanzi", "ton", "pinyin"])
        for hanzi, tone, pinyin in SYLLABLES:
            writer.writerow([hanzi, tone, pinyin])
    print(f"CSV écrit : {csv_path}  ({len(SYLLABLES)} entrées)")
    print()

    sem = asyncio.Semaphore(MAX_CONCURRENT)
    total_ok, total_skip, total_fail = 0, 0, 0

    for voice in VOICES:
        short = voice.replace("zh-CN-", "").replace("Neural", "")
        print(f"── {short} ({voice}) ──")
        ok, skip, fail = await generate_voice(voice, sem)
        total_ok += ok
        total_skip += skip
        total_fail += fail
        print(f"   ✓ {ok} générés   ⊘ {skip} déjà présents   ✗ {fail} erreurs")
        print()

    print("═" * 50)
    print(f"Total : {total_ok} générés, {total_skip} déjà présents, {total_fail} erreurs")
    if total_fail > 0:
        print(f"⚠️  {total_fail} fichiers en erreur — relancer le script pour réessayer")
        sys.exit(1)


if __name__ == "__main__":
    asyncio.run(main())
